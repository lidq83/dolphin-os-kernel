#include <mm.h>

/****************************************************************************
 * Public Functions
 ****************************************************************************/

/****************************************************************************
 * Name: mm_size2ndx
 *
 * Description:
 *    Convert the size to a nodelist index.
 *
 ****************************************************************************/

int mm_size2ndx(size_t size)
{
	int ndx = 0;

	if (size >= MM_MAX_CHUNK)
	{
		return MM_NNODES - 1;
	}

	size >>= MM_MIN_SHIFT;
	while (size > 1)
	{
		ndx++;
		size >>= 1;
	}

	return ndx;
}
